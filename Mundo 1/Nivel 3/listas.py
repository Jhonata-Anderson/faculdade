# Conteúdo inicial
lista_mesclada = [1, 2, 3, 'Olá, Python', True, 12.6]
print(lista_mesclada)

# Adicionando elemento na lista com append
lista_mesclada.append('Lista aninhada')
print(lista_mesclada)

# Inserindo o elemento numa posição específica com insert
lista_mesclada.insert(4, 5)
print(lista_mesclada)

# Mostrando a quantidade da lista com len
print(len(lista_mesclada))

# Retirando o elemento de uma posição especifica da lista com pop
lista_mesclada.pop(1)
print(lista_mesclada)

# Adicionando elementos especificos em uma nova lista
nova_lista_mesclada = lista_mesclada[0:5]
print(nova_lista_mesclada)

